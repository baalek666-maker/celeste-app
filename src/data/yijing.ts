// Lookup: 6-bit binary (bottom-to-top) => King Wen hexagram id
// Each line: 1=yang (solid), 0=yin (broken)
// Binary key = lower[0..2] + upper[0..2] as string
const LINE_TO_KINGWEN: Record<string, number> = {
  '111111': 1, '111110': 43, '111101': 14, '111100': 34, '111011': 9, '111010': 5, '111001': 26, '111000': 11,
  '110111': 10, '110110': 58, '110101': 38, '110100': 54, '110011': 61, '110010': 60, '110001': 41, '110000': 19,
  '101111': 13, '101110': 49, '101101': 30, '101100': 55, '101011': 37, '101010': 63, '101001': 22, '101000': 36,
  '100111': 25, '100110': 17, '100101': 21, '100100': 51, '100011': 42, '100010': 3, '100001': 27, '100000': 24,
  '011111': 44, '011110': 28, '011101': 50, '011100': 32, '011011': 57, '011010': 48, '011001': 18, '011000': 46,
  '010111': 6, '010110': 47, '010101': 64, '010100': 40, '010011': 59, '010010': 29, '010001': 4, '010000': 7,
  '001111': 33, '001110': 31, '001101': 56, '001100': 62, '001011': 53, '001010': 39, '001001': 52, '001000': 15,
  '000111': 12, '000110': 45, '000101': 35, '000100': 16, '000011': 20, '000010': 8, '000001': 23, '000000': 2,
};

export function hexagramFromLines(lines: number[]): number {
  const key = lines.map(l => l === 1 ? '1' : '0').join('');
  return LINE_TO_KINGWEN[key] || 1;
}

